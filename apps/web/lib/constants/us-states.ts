/** Allowlist of valid 2-letter state codes for Zod validation. */
export const US_STATE_CODES: readonly string[] = US_STATES.map((s) => s.code);

/** O(1) lookup for validation. */
const STATE_CODE_SET = new Set(US_STATE_CODES);

/** Returns true if value is a valid US state code (case-insensitive input, normalized to uppercase). */
export function isUsStateCode(value: unknown): value is string {
  if (typeof value !== 'string') return false;
  return STATE_CODE_SET.has(value.toUpperCase().trim());
}
